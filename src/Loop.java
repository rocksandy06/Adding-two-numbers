
public class Loop {

	public static void main(String[] args) {
		int x=5;
		while(x<=10) {
			System.out.println("while x="+x);
			x++;
		}
		do {
			System.out.println("do x="+x);
			x++;
		}while(x<=10);
		for(int y=5;y<=10;y++) {
			System.out.println("for y="+y);
		}
		
		
		
		
			
				
		

	}

}
