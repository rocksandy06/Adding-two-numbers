
public class Logical {

	public static void main(String[] args) {
		int a=20, b=7, c=3;
		System.out.println(a>b&&a>c);
		System.out.println(b>a||b>c);
		System.out.println(!(a>b));
		a+=5;
		System.out.println("a+="+a);
		b-=15;
		System.out.println("b-="+b);
		a*=2;
		System.out.println("a*="+a);
		c/=3;
		System.out.println("c/="+c);
		
		
		

	}

}
