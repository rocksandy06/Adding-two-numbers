
public class Bit {

	public static void main(String[] args) {
		int a=10,  b=5;
		System.out.println("a&b="+(a&b));
		System.out.println("a|b="+(a|b));
		a=21;
		a++;
		System.out.println("a>>1="+(a>>1));
		System.out.println("a>>>1="+(a>>>1));
		b=10;
		System.out.println("b<<1="+(b<<1));
		System.out.println("b<<2="+(b<<2));
		System.out.println("a="+a);
		System.out.println("~a="+(~a));
		
		
		
	}

}
